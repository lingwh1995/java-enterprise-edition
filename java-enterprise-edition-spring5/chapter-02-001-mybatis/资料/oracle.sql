CREATE SEQUENCE TBL_EMPLOYEE_SEQ
MINVALUE 1  --最小值
--NOMAXVALUE  --不设置最大值(由机器决定)，或 根据表字段的值范围设置 MAXVALUE
MAXVALUE 9999999  -- 最大值
START WITH 1   --从1开始计数，数值可变
INCREMENT BY 1  --每次加1，数值可变
NOCYCLE  --一直累加，不循环；CYCLE：达到最大值后，将从头开始累加
NOCACHE;

