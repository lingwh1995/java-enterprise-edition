CREATE TABLE TBL_EMPLOYEE (
    ID VARCHAR2(32) PRIMARY KEY,
    LAST_NAME VARCHAR2(50),
    EMAIL VARCHAR2(50),
    GENDER VARCHAR2(50)
);
